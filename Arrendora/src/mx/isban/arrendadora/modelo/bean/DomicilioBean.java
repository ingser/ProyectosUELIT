package mx.isban.arrendadora.modelo.bean;

public class DomicilioBean {
	
	private String tipo_domicilio;
	private String dom_extranjero;
	private String calle;
	private String num_ext;
	private String num_int;
	private String colonia;
	private String cp;
	private String cve_entidad;
	private String entidad;
	private String ciudad;
	public String getTipo_domicilio() {
		return tipo_domicilio;
	}
	public void setTipo_domicilio(String tipo_domicilio) {
		this.tipo_domicilio = tipo_domicilio;
	}
	public String getDom_extranjero() {
		return dom_extranjero;
	}
	public void setDom_extranjero(String dom_extranjero) {
		this.dom_extranjero = dom_extranjero;
	}
	public String getCalle() {
		return calle;
	}
	public void setCalle(String calle) {
		this.calle = calle;
	}
	public String getNum_ext() {
		return num_ext;
	}
	public void setNum_ext(String num_ext) {
		this.num_ext = num_ext;
	}
	public String getNum_int() {
		return num_int;
	}
	public void setNum_int(String num_int) {
		this.num_int = num_int;
	}
	public String getColonia() {
		return colonia;
	}
	public void setColonia(String colonia) {
		this.colonia = colonia;
	}
	public String getCp() {
		return cp;
	}
	public void setCp(String cp) {
		this.cp = cp;
	}
	public String getCve_entidad() {
		return cve_entidad;
	}
	public void setCve_entidad(String cve_entidad) {
		this.cve_entidad = cve_entidad;
	}
	public String getEntidad() {
		return entidad;
	}
	public void setEntidad(String entidad) {
		this.entidad = entidad;
	}
	public String getCiudad() {
		return ciudad;
	}
	public void setCiudad(String ciudad) {
		this.ciudad = ciudad;
	}

	

}
